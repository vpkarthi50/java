package daily_task;

public class type_casting {
    public static void main(String[] args) {
       //wideing method int to double
        int a=20;
        double b=a;
        System.out.println(a);
        System.out.println(b);
        //narrowing method double to float
        float c=50.52f;
        double d=(double) c;
        System.out.println(c);
        System.out.println(d);
        //Using char to find  the ASCII value
        char e=90;
        System.out.println(e); 
    }
}
