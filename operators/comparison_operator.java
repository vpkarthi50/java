package daily_task.operators;

public class comparison_operator {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(a==10);
        System.out.println(b!=5);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=5);
        System.out.println(a<=10);
    }
}
