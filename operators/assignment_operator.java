package daily_task.operators;

public class assignment_operator {
    public static void main(String[] args) {
        int a=10;
            a+=20;
        System.out.println(a);
        int b=5;
            b*=2;
        System.out.println(b);
        int c=10;
            c/=2;
        System.out.println(c);
        int d=10;
            d%=2;
        System.out.println(d);
        int e=10;
            e^=3;
        System.out.println(e);
    }
}
